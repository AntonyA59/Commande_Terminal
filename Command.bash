# Liste des éléments dans le dossier où on se trouve. Si on ajoute le flag "-la" la liste à une autre forme.
ls

# Permet d'indiquer le chemin du dossier dans lequel on se trouve.
pwd

# Naviguer dans le dossier sélectionné.
cd <nomDuDossier>

# Permet de nettoyer l'invite de commande.
clear

# Créer un fichier
Touch <nomDuFichier>

# Supprimer un fichier. Avec le flag "-r" on peut supprimer un dossier.
rm <nomDuFichier>

# Générer une clé SSH RSA
ssh-keygen -t rsa
